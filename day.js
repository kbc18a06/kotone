function dCheck1() {//チェックボックス
    let che1 = document.challenge.clg1;
    let target = document.getElementById("dtarget1");
    if (che1.checked) {
        $('#dtarget1').css('color', '#b8b8b8');
        //target.disabled = true;
    } else {
        $('#dtarget1').css('color', 'black');
        //target.disabled = false;
    }
}

